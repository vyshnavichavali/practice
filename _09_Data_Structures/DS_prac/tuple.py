""" methods: count(), index()
# Functions: length(), sorted(), sum(), min(), max() """

# Methods
tup = (1, 2,  3, 4, 2, 5, 6)
count = tup.count(3)
print("count is:", count)

tup = (1, 2, 3, 4, 5)
ind = tup.index(4)
print("index is: ", ind)

# Functions
tup = (5, 9, 6, 7, 3, 53, 4, 2, 6, 5)
length = len(tup)
print("length is: ", length)

tup = (5, 9, 6, 7, 3, 53)
sort = sorted(tup)
print("sorted tuple is : ", sort)

